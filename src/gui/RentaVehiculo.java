    public void cargarOficina() {
        OficinaDAO dao = new OficinaDAO();
        LinkedList<Oficina> oficinas = dao.cargarTodo();
        for (Oficina oficina : oficinas) {
            CbxOfiRetiro.addItem(oficina.getNombre());
            CbxOfiDevol.addItem(oficina.getNombre());
        }
    }

