    public void adicionales() {
        int adi = 0;
        if (rbtGps.isSelected()) {
            adi = adi + 9;
        }
        if (rbtBooster.isSelected()) {
            adi = adi + 11;
        }
        if (rbtSilla.isSelected()) {
            adi = adi + 3;
        }
        adicionales = adi;

    }

